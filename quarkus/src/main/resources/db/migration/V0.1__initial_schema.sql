    status               varchar(255),
    payment_method       varchar(255),
    payment_status       varchar(255),
    value_gross          numeric(19, 2) not null,
    value_nett           numeric(19, 2) not null,
    shipping_address_id  uuid,
    user_id              uuid,
    simple_products_json text, -- >> TODO here switch to jsonb later
