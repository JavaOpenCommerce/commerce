(
    id                bigint generated by default as identity
        constraint warehouse_item_details_pk primary key,
    short_description text,
    location          varchar(255),
    unit_weight       double precision,
    unit_dimension    varchar(255)
);

alter table ${flyway:defaultSchema}.warehouse_item_details
    owner to ${flyway:user};

create table warehouse_item
(
    id                        bigint unique not null
        constraint warehouse_item_pk primary key,
    stock                     integer       not null check (stock >= 0),
    warehouse_item_details_id bigint
        constraint warehouse_item_details_fk references warehouse_item_details,
    constraint warehouse_item_fk foreign key (id) references item
);

alter table ${flyway:defaultSchema}.warehouse_item
    owner to ${flyway:user};

